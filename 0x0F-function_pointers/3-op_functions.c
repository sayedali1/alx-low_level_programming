#include "3-calc.h"

/**
*op_add - fun that add two nums
*@a:first num
*@b:sec num
*Return: int
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
*op_sub - fun that sub two nums
*@a:first num
*@b:sec num
*Return: int
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
*op_mul - fun that mul two nums
*@a:first num
*@b:sec num
*Return: int
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
*op_div - fun that div two nums
*@a:first num
*@b:sec num
*Return: int
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
*op_mod - fun that mod two nums
*@a:first num
*@b:sec num
*Return: int
*/
int op_mod(int a, int b)
{
	return (a % b);
}
